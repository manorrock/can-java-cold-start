package coldstart.shared;

/**
 * A model object for job measurements.
 * 
 * @author Manfred Riem (mriem@manorrock.com)
 */
public class JobMeasurement {
    
    /**
     * Stores the name.
     */
    private String name;

    /**
     * Get the name.
     * 
     * @return the name.
     */
    public String getName() {
        return name;
    }

    /**
     * Set the name.
     * 
     * @param name the name.
     */
    public void setName(String name) {
        this.name = name;
    }
}
